package mypackage;

public class RelationalOperator {
public static void main(String args[]) {
	int x=20;
	int y=50;
	boolean z=x<y;
	System.out.println(x>y);
	System.out.println(x>=y);
	System.out.println(x<y);
	System.out.println(x<=y);
	System.out.println(x!=y);
	System.out.println(x==y);
	System.out.println(z);
}
}
